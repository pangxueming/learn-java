package learnObjectClass;

public class TestIfElse {
  public static void main(String[] args) {
    int num = 10;

    if (num < 20) {
      System.out.println("num 大于20: num = " + num);
      num = 21;
    } else {
      System.out.println(1111);
    }
    System.out.println("num 大于20: num = " + num);
  }
}
